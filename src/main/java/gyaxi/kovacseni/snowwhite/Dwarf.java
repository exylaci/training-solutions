package gyaxi.kovacseni.snowwhite;

public class Dwarf {
    private final String name;
    private final int age;

    public Dwarf(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}